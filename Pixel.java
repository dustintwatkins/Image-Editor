
public class Pixel {
	
	private int red;
	private int green;
	private int blue;
	
	public void setRed(int val)
	{
		red = val;
	}
	public void setGreen(int val)
	{
		green = val;
	}
	public void setBlue(int val)
	{
		blue = val;
	}
	public int getRed()
	{
		return red;
	}
	public int getGreen()
	{
		return green;
	}
	public int getBlue()
	{
		return blue;
	}
	
}//end Pixel class



